CREATE TABLE AHPATIENT
(

	PATIENTID NUMBER(19,0) NOT NULL,
	IDENTIFICATIONNO VARCHAR2(20) NOT NULL,
	PATIENTNO VARCHAR2(20) NOT NULL,
	FIRSTNAME VARCHAR2(64), 
	LASTNAME VARCHAR2(64),
	BIRTHDATE DATE,
	MARITALSTATUSID varchar2(20),
	BLOODGROUPID varchar2(20),
	GENDERID VARCHAR2(20),
	STATUS VARCHAR2(1),
	CONSTRAINT AHPATIENT_PK PRIMARY KEY (PATIENTID)
);

CREATE TABLE AHORGANIZATION
(
	ORGANIZATIONID NUMBER(19,0) NOT NULL,
	NAME VARCHAR2(64), 
	CODE VARCHAR2(64),
	ACTIVE VARCHAR2(1),
	STATUS VARCHAR2(1),
	CONSTRAINT AHORGANIZATION_PK PRIMARY KEY (ORGANIZATIONID)
);


CREATE TABLE AHSTAFFTITLE
(
	STAFFTITLEID NUMBER(19,0) NOT NULL,
	CODE VARCHAR2(64),
	NAME VARCHAR2(64), 
	STATUS VARCHAR2(1),
	CONSTRAINT AHSTAFFTITLE_PK PRIMARY KEY (STAFFTITLEID)
);


	
